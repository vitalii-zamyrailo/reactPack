/**
 * Represents a book.
 * @constructor
 * @param {string} title - The title of the book.
 * @param {string} author - The author of the book.
 * @param {number} price - The price of the book.
 */
class Book {
	constructor(title, author, price) {
		this.title = title;
		this.author = author;
		this.price = price;
	}
}
